package org.icpc.tools.contest.model;

/**
 * An object that has an x and y position.
 */
public interface IPosition {
	/**
	 * The x position of the object.
	 *
	 * @return the x position
	 */
	public double getX();

	/**
	 * The y position of the object.
	 *
	 * @return the y position
	 */
	public double getY();
}